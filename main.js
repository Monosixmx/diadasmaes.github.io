document.getElementById('inputjs').addEventListener('input', function() {
    const searchTerm = this.value.toLowerCase(); // Obtém o valor digitado e converte para minúsculas
    const alunos = document.querySelectorAll('ul li'); // Seleciona todos os itens da lista
    alunos.forEach(function(aluno) {
        const alunoText = aluno.textContent.toLowerCase(); // Obtém o texto do item e converte para minúsculas
        if (alunoText.includes(searchTerm)) {
            aluno.style.display = 'block';
            aluno.style.margin = '0, auto';// Mostra o item se ele corresponder ao termo de busca
        } else {
            aluno.style.display = 'none'; // Oculta o item se ele não corresponder
        }
    });
});

